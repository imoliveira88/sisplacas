    private void addActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_addActionPerformed
        
        
        Componente c = new Componente();
        //c.setPlaca(p);
        c.setTipo((String) tipoComponente.getSelectedItem());
        c.setRotulo(rotulo.getText());
        c.setValor(valor.getText());
        c.setDescricao(descricao.getText());
        
        componentes.add(c);
    }//GEN-LAST:event_addActionPerformed

    private void salvarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_salvarActionPerformed
        Placa p = new Placa();
        p.setDescricao(descricaoPlaca.getText());
        p.setNumero(numeroPlaca.getText());
        for(int i=0; i < componentes.size(); i++) componentes.get(i).setPlaca(p);
        
        p.setComponentes(componentes);
        
        (new PlacaServico()).save(p);
    }//GEN-LAST:event_salvarActionPerformed

